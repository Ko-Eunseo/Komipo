
// gnb open and close
function openNcloseNav(){
  var clickbtn = document.getElementById("gnbBox");
  if(clickbtn.style.display === "none"){
      clickbtn.style.display = "block";
  }else{
      clickbtn.style.display = "none";
  }
}

